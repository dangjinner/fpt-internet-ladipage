            // Thêm validate cho form mới
            $(newForm).validate({
                rules: {
                    name: {required: true},
                    phone: {required: true, phoneVN: true},
                    address: {required: true},
                    service: {required: true}
                },
                errorPlacement: () => true, // Không append error message
                highlight: function (element) {
                    $(element).closest('.ladi-form-item').addClass('input-error');
                },
                unhighlight: function (element) {
                    $(element).closest('.ladi-form-item').removeClass('input-error');
                },
                submitHandler: function (form) {
                    const $btn = $(form).find('.btn-submit');
                    const $text = $btn.find('p');

                    // Ẩn text, thêm loading
                    $text.hide();
                    if ($btn.find('.loading-spinner').length === 0) {
                        $btn.append('<div class="loading-spinner"></div>');
                    }

                    const formData = {
                        name: $(form).find('[name="name"]').val(),
                        phone: $(form).find('[name="phone"]').val(),
                        address: $(form).find('[name="address"]').val(),
                        service: $(form).find('[name="service"]').val(),
                        utm_source: getQueryParam('utm_source'),
                        utm_medium: getQueryParam('utm_medium'),
                        utm_campaign: getQueryParam('utm_campaign'),
                        utm_term: getQueryParam('utm_term'),
                        utm_content: getQueryParam('utm_content')
                    };

                    $.ajax({
                        url: "{{ route('fpt.internet.register') }}",
                        method: 'POST',
                        data: formData,
                        headers: {
                            'X-CSRF-TOKEN': $('meta[name="csrf-token"]').attr('content')
                        },
                        success: function (response) {
                            window.location.href = '{{ route('fpt.internet.thank-you') }}';
                        },
                        error: function (xhr) {
                            alert('Có lỗi xảy ra, vui lòng thử lại.');
                        },
                        complete: function () {
                            // Bỏ loading, show lại text
                            $btn.find('.loading-spinner').remove();
                            $text.show();
                        }
                    });
                }
            });
        });
    }

    $.validator.addMethod("phoneVN", function (value, element) {
        return this.optional(element) || /^(0[2-9]|84[2-9])\d{8}$/.test(value);
    }, "Số điện thoại không hợp lệ");

    document.addEventListener('DOMContentLoaded', function () {
        resetFormWithValidation('.register-service-form');
        resetSubmitButtons();
    });
